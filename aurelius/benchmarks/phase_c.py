"""Phase C — fair, three-way, on-demand-priced benchmark harness.

Implements the three-way comparison mandated by
``research/OPTIMIZER_UNIFICATION_PLAN.md`` Governance §1 — **Current-Main vs
Best-Aurelius vs Candidate** — which previously existed only as governance text
with no harness backing (every serving/replica backtest was pairwise).

Design (audit 2026-06-25):
- **Fair baseline.** "Current-Main" is the strongest realistic *deployable* safe
  baseline an operator would actually run (e.g. lag-1 reactive sizing), not FIFO
  and not an under-provisioned fixed-c oracle (``docs/RESULTS.md`` §3).
- **On-demand denominator.** Every arm is priced on the pure on-demand cost path
  (``forecasted_mcs.evaluate_c_schedule`` → ``sum(c)·tick_hr·GPU_HOUR_USD``) — NO
  spot discount. This re-measures OSOTSS / forecasted_mcs without the spot-fleet
  cost trick that ``docs/RESULTS.md`` §3.1 demotes.
- **Reproducible.** Every result serializes the ``seed`` and a SHA-256
  trace-content hash, so any artifact can be regenerated byte-for-byte.
- **Honest classification.** Each arm carries ``deployable`` / ``uses_future_info``
  flags; oracle arms (e.g. AMCSG, SOTSS-MIN) are allowed as *reference* but are
  labelled non-deployable and never presented as the deployable winner.

Reuses the frozen evaluator + canonical objective; adds no new optimization
logic. Scope: the deployable replica-scaling policies (the set that is honestly
benchmarkable on public arrivals+tokens data).
"""

from __future__ import annotations

import hashlib
import json
from dataclasses import dataclass
from typing import Optional

from ..optimizer.layers import ObjectiveLayer
from .forecasted_mcs import (
    GPU_HOUR_USD,
    evaluate_c_schedule,
    forecast_mcs_c_schedule,
    reactive_lag1_c_schedule,
)

#: Allowed three-way roles.
ROLE_CURRENT_MAIN = "current_main"
ROLE_BEST_AURELIUS = "best_aurelius"
ROLE_CANDIDATE = "candidate"
_ROLES = frozenset({ROLE_CURRENT_MAIN, ROLE_BEST_AURELIUS, ROLE_CANDIDATE})


def trace_content_hash(
    raw: list, *, tick_seconds: float, warp: float, sla_s: float
) -> str:
    """SHA-256[:16] over the trace content + its evaluation provenance.

    Two runs with the same ``(arrivals, tokens, tick_seconds, warp, sla_s)``
    produce the same hash — the reproducibility key Phase C serializes into
    every artifact (public traces have no frozen-YAML scenario hash).
    """
    h = hashlib.sha256()
    h.update(
        json.dumps(
            {
                "n": len(raw),
                "tick_seconds": round(float(tick_seconds), 9),
                "warp": round(float(warp), 9),
                "sla_s": round(float(sla_s), 9),
            },
            sort_keys=True,
        ).encode()
    )
    for arr, tok in raw:
        h.update(f"{round(float(arr), 6)}:{int(tok)};".encode())
    return h.hexdigest()[:16]


@dataclass
class ArmSpec:
    """One arm of a three-way comparison (a named, pre-computed c_schedule)."""

    name: str
    role: str  # current_main | best_aurelius | candidate
    c_schedule: list  # per-tick replica counts
    uses_future_info: bool
    deployable: bool
    classification: str
    discipline: str = "fifo"

    def __post_init__(self):
        if self.role not in _ROLES:
            raise ValueError(
                f"ArmSpec.role must be one of {sorted(_ROLES)}; got {self.role!r}"
            )


@dataclass
class ThreeWayArmResult:
    name: str
    role: str
    deployable: bool
    uses_future_info: bool
    goodput_per_dollar: float
    cost_usd: float
    gpu_hours: float
    n_sla_safe: int
    sla_violations: int
    kpi: dict

    def to_dict(self) -> dict:
        return {
            "name": self.name,
            "role": self.role,
            "deployable": self.deployable,
            "uses_future_info": self.uses_future_info,
            "goodput_per_dollar": round(self.goodput_per_dollar, 4),
            "cost_usd": round(self.cost_usd, 4),
            "gpu_hours": round(self.gpu_hours, 4),
            "n_sla_safe": self.n_sla_safe,
            "sla_violations": self.sla_violations,
            "kpi": self.kpi,
        }


@dataclass
class ThreeWayResult:
    """A fair, on-demand-priced, reproducible three-way comparison."""

    trace_id: str
    seed: int
    trace_hash: str
    sla_s: float
    tick_seconds: float
    warp: float
    denominator: str
    gpu_hour_usd: float
    arms: list  # list[ThreeWayArmResult]
    ranking: list  # [(name, goodput_per_dollar)] desc
    winner: Optional[str]
    deployable_winner: Optional[str]
    notes: tuple = ()

    def to_dict(self) -> dict:
        return {
            "trace_id": self.trace_id,
            "seed": self.seed,
            "trace_hash": self.trace_hash,
            "sla_s": self.sla_s,
            "tick_seconds": self.tick_seconds,
            "warp": round(self.warp, 9),
            "denominator": self.denominator,
            "gpu_hour_usd": self.gpu_hour_usd,
            "arms": [a.to_dict() for a in self.arms],
            "ranking": [[n, round(g, 4)] for n, g in self.ranking],
            "winner": self.winner,
            "deployable_winner": self.deployable_winner,
            "notes": list(self.notes),
        }


def run_three_way(
    raw: list,
    arm_specs: list,
    *,
    tick_seconds: float,
    warp: float,
    sla_s: float,
    seed: int,
    trace_id: str,
    notes=(),
) -> ThreeWayResult:
    """Evaluate every arm on the SAME trace + on-demand denominator and rank.

    Args:
        raw: ``(arrival_s_unwarped, output_tokens)`` tuples.
        arm_specs: list of :class:`ArmSpec` (typically 3: one per role).
        seed: RNG seed recorded for reproducibility (the evaluator is
            deterministic; seed is provenance, mirrored from the trace run).
        trace_id: stable trace identifier (e.g. ``"azure_llm_2024_sample"``).

    Returns:
        :class:`ThreeWayResult` — ranked by SLA-safe goodput/$ via the canonical
        :class:`ObjectiveLayer`, plus the best *deployable* arm separately (so an
        oracle reference can never masquerade as the shippable winner).
    """
    obj = ObjectiveLayer()
    arms: list[ThreeWayArmResult] = []
    for spec in arm_specs:
        kpi = evaluate_c_schedule(
            raw,
            spec.c_schedule,
            tick_seconds,
            warp,
            sla_s,
            policy=spec.name,
            uses_future_info=spec.uses_future_info,
            deployable=spec.deployable,
            classification=spec.classification,
            discipline=spec.discipline,
        )
        arms.append(
            ThreeWayArmResult(
                name=spec.name,
                role=spec.role,
                deployable=spec.deployable,
                uses_future_info=spec.uses_future_info,
                goodput_per_dollar=kpi.goodput_per_dollar,
                cost_usd=kpi.cost_usd,
                gpu_hours=kpi.gpu_hours,
                n_sla_safe=kpi.n_sla_safe,
                sla_violations=kpi.sla_violations,
                kpi=kpi.to_dict(),
            )
        )

    ranking = obj.compare({a.name: a.goodput_per_dollar for a in arms})
    winner = ranking[0][0] if ranking else None
    deployable_ranking = obj.compare(
        {a.name: a.goodput_per_dollar for a in arms if a.deployable}
    )
    deployable_winner = deployable_ranking[0][0] if deployable_ranking else None

    return ThreeWayResult(
        trace_id=trace_id,
        seed=seed,
        trace_hash=trace_content_hash(
            raw, tick_seconds=tick_seconds, warp=warp, sla_s=sla_s
        ),
        sla_s=sla_s,
        tick_seconds=tick_seconds,
        warp=warp,
        denominator="on_demand",
        gpu_hour_usd=GPU_HOUR_USD,
        arms=arms,
        ranking=ranking,
        winner=winner,
        deployable_winner=deployable_winner,
        notes=tuple(notes),
    )


def standard_replica_scaling_arms(
    raw: list,
    tick_seconds: float,
    warp: float,
    sla_s: float,
    *,
    mcs_gate: float = 12.5,
) -> list:
    """The deployable, on-demand, FAIR three-way for replica scaling.

    All arms are causal (no oracle), priced on-demand:
      * ``current_main``  = lag-1 reactive sizing (what an operator runs WITHOUT
        forecasting — the strongest realistic deployable safe baseline);
      * ``best_aurelius`` = ``forecasted_mcs`` EWMA (validated deployable causal);
      * ``candidate``     = ``forecasted_mcs`` quantile (deployable causal variant).

    This is the honest re-measurement of forecasted_mcs *without* the spot-fleet
    cost model.
    """
    lag1 = reactive_lag1_c_schedule(
        raw, tick_seconds, warp, mcs_gate=mcs_gate, sla_s=sla_s
    )
    ewma, _diag_e = forecast_mcs_c_schedule(
        raw, tick_seconds, warp, method="ewma", mcs_gate=mcs_gate, sla_s=sla_s
    )
    quant, _diag_q = forecast_mcs_c_schedule(
        raw, tick_seconds, warp, method="quantile", mcs_gate=mcs_gate, sla_s=sla_s
    )
    return [
        ArmSpec(
            "reactive_lag1", ROLE_CURRENT_MAIN, lag1,
            uses_future_info=False, deployable=True,
            classification="deployable_reactive_baseline",
        ),
        ArmSpec(
            "forecasted_mcs_ewma", ROLE_BEST_AURELIUS, ewma,
            uses_future_info=False, deployable=True,
            classification="deployable_causal_forecast",
        ),
        ArmSpec(
            "forecasted_mcs_quantile", ROLE_CANDIDATE, quant,
            uses_future_info=False, deployable=True,
            classification="deployable_causal_forecast",
        ),
    ]


